#!/usr/bin/env node

/**
 * @libra/mcp-server — MCP Server for Libra Design System
 *
 * Exposes Libra's design tokens and component generation to AI agents.
 * Allows AI to generate UI code that follows Libra's design language.
 *
 * Tools:
 *   get_tokens         — Return all CSS custom properties with values
 *   get_semantic_colors — Return the semantic color palette (up/down/ma/etc)
 *   get_typography     — Return font stacks and size scale
 *   get_spacing        — Return spacing scale
 *   get_components     — Return component specs (button/card/table)
 *   generate_css       — Generate a complete tokens.css for a project
 *   generate_component — Generate a React component following Libra design
 *
 * @see https://github.com/goodie1972/libra
 */

import { Server } from '@modelcontextprotocol/sdk/server/index.js';
import { StdioServerTransport } from '@modelcontextprotocol/sdk/server/stdio.js';
import {
  CallToolRequestSchema,
  ListToolsRequestSchema,
} from '@modelcontextprotocol/sdk/types.js';

