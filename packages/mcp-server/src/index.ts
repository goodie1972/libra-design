  '--up': '#ef5350', '--down': '#26a69a', '--flat': '#9e9e9e',
  '--up-bg': 'rgba(239,83,80,0.08)', '--down-bg': 'rgba(38,166,154,0.08)',
  '--ma5': '#f8b500', '--ma10': '#4a6cf7', '--ma20': '#9c27b0', '--ma60': '#009688', '--ma120': '#ff7043', '--ma250': '#78909c',
  '--success': '#34a853', '--warning': '#fbbc04', '--error': '#ea4335',
  '--bg-root': '#0c0c0e', '--bg-main': '#0c0c0e', '--bg-card': '#121214',
  '--bg-card-hover': '#18181b', '--bg-input': '#18181b',
  '--border-main': '#1e1e22', '--border-sub': '#161618',
  '--text-primary': '#e8e8ed', '--text-secondary': '#9a9aa0', '--text-tertiary': '#63636a',
